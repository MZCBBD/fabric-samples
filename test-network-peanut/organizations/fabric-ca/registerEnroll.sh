#!/bin/bash

function createdid() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/did.byondz.io/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/did.byondz.io/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-did --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-did.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-did.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-did.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-did.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy did's CA cert to did's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/did/ca-cert.pem" "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/tlscacerts/ca.crt"

  # Copy did's CA cert to did's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/did.byondz.io/tlsca"
  cp "${PWD}/organizations/fabric-ca/did/ca-cert.pem" "${PWD}/organizations/peerOrganizations/did.byondz.io/tlsca/tlsca.did.byondz.io-cert.pem"

  # Copy did's CA cert to did's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/did.byondz.io/ca"
  cp "${PWD}/organizations/fabric-ca/did/ca-cert.pem" "${PWD}/organizations/peerOrganizations/did.byondz.io/ca/ca.did.byondz.io-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-did --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering peer1"
  set -x
  fabric-ca-client register --caname ca-did --id.name peer1 --id.secret peer1pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-did --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-did --id.name didadmin --id.secret didadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-did -M "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/msp" --csr.hosts peer0.did.byondz.io --csr.names C=KR,ST=Seoul,OU=byondz.io --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/config.yaml" "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/msp/config.yaml"

  infoln "Generating the peer0-tls certificates"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-did -M "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls" --enrollment.profile tls --csr.hosts peer0.did.byondz.io --csr.hosts localhost --csr.hosts "*.did.byondz.io" --csr.names C=KR,ST=Seoul,OU=byondz.io --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  set -x
  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls/keystore/"* "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer0.did.byondz.io/tls/server.key"
  { set +x;} 2>/dev/null

  infoln "Generating the peer1 msp"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-did -M "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/msp" --csr.hosts peer1.did.byondz.io --csr.names C=KR,ST=Seoul,OU=byondz.io --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/config.yaml" "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/msp/config.yaml"

  infoln "Generating the peer1-tls certificates"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-did -M "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls" --enrollment.profile tls --csr.hosts peer1.did.byondz.io --csr.hosts localhost --csr.hosts "*.did.byondz.io" --csr.names C=KR,ST=Seoul,OU=byondz.io --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  set -x
  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls/keystore/"* "${PWD}/organizations/peerOrganizations/did.byondz.io/peers/peer1.did.byondz.io/tls/server.key"
  { set +x;} 2>/dev/null
  
  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-did -M "${PWD}/organizations/peerOrganizations/did.byondz.io/users/User1@did.byondz.io/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/config.yaml" "${PWD}/organizations/peerOrganizations/did.byondz.io/users/User1@did.byondz.io/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://didadmin:didadminpw@localhost:7054 --caname ca-did -M "${PWD}/organizations/peerOrganizations/did.byondz.io/users/Admin@did.byondz.io/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/did/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/did.byondz.io/msp/config.yaml" "${PWD}/organizations/peerOrganizations/did.byondz.io/users/Admin@did.byondz.io/msp/config.yaml"
}

